
package sistemagestion;


public class Curso {
    
    String nombreC;
    String profesorDesignado;

    public Curso() {
    }

    public Curso(String nombreC, String profesorDesignado) {
        this.nombreC = nombreC;
        this.profesorDesignado = profesorDesignado;
    }

    public String getNombreC() {
        return nombreC;
    }

    public void setNombreC(String nombreC) {
        this.nombreC = nombreC;
    }

    public String getProfesorDesignado() {
        return profesorDesignado;
    }

    public void setProfesorDesignado(String profesorDesignado) {
        this.profesorDesignado = profesorDesignado;
    }
    
    
    
    
    
    
}
